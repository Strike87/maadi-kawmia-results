import { NextRequest, NextResponse } from 'next/server';
import { GRADE_MAP, getErrorMessage, mapGasError } from '@/lib/constants';

// Server-only: Google Apps Script endpoint — loaded from env, never hardcoded in source
const API_URL = process.env.GAS_API_URL || '';

const GAS_API_KEY = process.env.GAS_API_KEY || '';

const TURNSTILE_SECRET_KEY = process.env.TURNSTILE_SECRET_KEY || '';

const IS_PRODUCTION = process.env.NODE_ENV === 'production';

// Allowed fields to pass through from GAS response (whitelist for sanitization)
const ALLOWED_FIELDS = new Set([
  'termName', 'cl', 'clLabel', 'id', 'stn',
  'headers', 'scores', 'maxScores', 'terms', 'activeSheets',
]);

// Sanitize a string value — strip potential XSS characters
function sanitizeString(value: unknown): string {
  return String(value || '').replace(/[<>"'&]/g, '');
}

// Sanitize GAS response — only allow whitelisted fields
function sanitizeResponse(data: Record<string, unknown>): Record<string, unknown> {
  const sanitized: Record<string, unknown> = {};
  for (const [key, value] of Object.entries(data)) {
    if (ALLOWED_FIELDS.has(key)) {
      // Sanitize string fields that will be displayed in the UI
      if (key === 'stn') {
        sanitized[key] = sanitizeString(value);
      } else {
        sanitized[key] = value;
      }
    }
  }
  return sanitized;
}

// Validate origin for defense-in-depth against CSRF
function isValidOrigin(request: NextRequest): boolean {
  const origin = request.headers.get('origin');
  // Allow requests with no origin (mobile apps, curl, server-to-server)
  if (!origin) return true;
  // Must match our domain
  return (
    origin.endsWith('maadi-kawmia-results.vercel.app') ||
    origin.startsWith('http://localhost:')
  );
}

async function verifyTurnstile(token: string): Promise<boolean> {
  // Dev mode bypass: allow the dev-bypass-token generated by Turnstile component
  // when NEXT_PUBLIC_TURNSTILE_SITE_KEY is not configured
  if (!IS_PRODUCTION && token === 'dev-bypass-token') {
    console.warn('Turnstile: dev-bypass-token accepted (development only)');
    return true;
  }

  // Fail closed: if no secret key, CAPTCHA verification fails
  if (!TURNSTILE_SECRET_KEY) {
    console.error('TURNSTILE_SECRET_KEY not set — captcha verification denied');
    return false;
  }

  try {
    const res = await fetch(
      'https://challenges.cloudflare.com/turnstile/v0/siteverify',
      {
        method: 'POST',
        headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
        body: new URLSearchParams({
          secret: TURNSTILE_SECRET_KEY,
          response: token,
        }),
      }
    );

    const data = await res.json();
    return data.success === true;
  } catch (error) {
    if (IS_PRODUCTION) {
      console.error('Turnstile verification failed');
    } else {
      console.error('Turnstile verification error:', error);
    }
    return false;
  }
}

export async function GET() {
  return NextResponse.json({ message: 'Hello, world!' });
}

export async function POST(request: NextRequest) {
  // ── Origin validation (defense-in-depth) ──
  if (!isValidOrigin(request)) {
    return NextResponse.json(
      { error: 'Forbidden' },
      { status: 403 }
    );
  }

  try {
    const body = await request.json();
    const { action, termName, cls, roll, captchaToken } = body;

    // ─── getTermNames ───
    if (action === 'getTermNames') {
      if (!API_URL) {
        console.error('GAS_API_URL env variable not set');
        return NextResponse.json(
          { error: getErrorMessage('SERVER_ERROR') },
          { status: 500 }
        );
      }

      const res = await fetch(API_URL, {
        method: 'POST',
        headers: { 'Content-Type': 'text/plain;charset=utf-8' },
        body: JSON.stringify({ action: 'getTermNames' }),
      });

      if (!res.ok) {
        if (IS_PRODUCTION) {
          console.error('Google API error');
        } else {
          console.error(`Google API error: ${res.status}`);
        }
        return NextResponse.json(
          { error: getErrorMessage('SERVER_ERROR') },
          { status: 502 }
        );
      }

      const data = await res.json();

      // Pass through GAS errors with mapping
      if (data.error) {
        return NextResponse.json(
          { error: getErrorMessage(data.error) },
          { status: 502 }
        );
      }

      // Cache getTermNames response for 5 minutes — this data rarely changes
      return NextResponse.json(data, {
        headers: {
          'Cache-Control': 'public, s-maxage=300, stale-while-revalidate=600',
        },
      });
    }

    // ─── getStudentData ───
    if (action === 'getStudentData') {
      // ── Validate required fields ──
      if (!termName || !cls || !roll) {
        return NextResponse.json(
          { error: getErrorMessage('MISSING_FIELDS') },
          { status: 400 }
        );
      }

      // ── Validate termName (must be a non-empty string) ──
      if (typeof termName !== 'string' || !termName.trim()) {
        return NextResponse.json(
          { error: getErrorMessage('INVALID_TERM') },
          { status: 400 }
        );
      }

      // ── Validate grade (must be in GRADE_MAP) ──
      if (!(cls in GRADE_MAP)) {
        return NextResponse.json(
          { error: getErrorMessage('INVALID_GRADE') },
          { status: 400 }
        );
      }

      // ── Validate national ID ──
      if (!/^[0-9]{14}$/.test(String(roll).trim())) {
        return NextResponse.json(
          { error: getErrorMessage('INVALID_ID') },
          { status: 400 }
        );
      }

      // ── Verify Turnstile captcha (REQUIRED — fail closed) ──
      if (!captchaToken) {
        return NextResponse.json(
          { error: getErrorMessage('MISSING_CAPTCHA') },
          { status: 403 }
        );
      }

      const isValidCaptcha = await verifyTurnstile(captchaToken);
      if (!isValidCaptcha) {
        return NextResponse.json(
          { error: getErrorMessage('CAPTCHA_FAILED') },
          { status: 403 }
        );
      }

      // ── Validate API key is configured ──
      if (!GAS_API_KEY) {
        console.error('GAS_API_KEY env variable not set');
        return NextResponse.json(
          { error: getErrorMessage('SERVER_ERROR') },
          { status: 500 }
        );
      }

      if (!API_URL) {
        console.error('GAS_API_URL env variable not set');
        return NextResponse.json(
          { error: getErrorMessage('SERVER_ERROR') },
          { status: 500 }
        );
      }

      const res = await fetch(API_URL, {
        method: 'POST',
        headers: { 'Content-Type': 'text/plain;charset=utf-8' },
        body: JSON.stringify({
          action: 'getStudentData',
          termName,
          cls,
          roll,
          apiKey: GAS_API_KEY,
        }),
      });

      // ── Handle HTTP-level errors from GAS ──
      if (!res.ok) {
        if (IS_PRODUCTION) {
          console.error('Google API error');
        } else {
          console.error(`Google API error: ${res.status}`);
        }

        // 429 = rate limited by GAS backend
        if (res.status === 429) {
          return NextResponse.json(
            { error: getErrorMessage('RATE_LIMITED') },
            { status: 429 }
          );
        }

        return NextResponse.json(
          { error: getErrorMessage('SERVER_ERROR') },
          { status: 502 }
        );
      }

      // ── Parse GAS response and map any error ──
      let data: Record<string, unknown>;
      try {
        data = await res.json();
      } catch {
        return NextResponse.json(
          { error: getErrorMessage('DATA_READ_ERROR') },
          { status: 502 }
        );
      }

      if (data.error) {
        const rawError = String(data.error);
        const mappedError = getErrorMessage(rawError);
        // Determine appropriate HTTP status based on mapped error key
        const errorKey = mapGasError(rawError);
        let status = 400;

        if (errorKey === 'RESULTS_UNAVAILABLE') {
          status = 403;
        } else if (errorKey === 'RATE_LIMITED') {
          status = 429;
        } else if (errorKey === 'NO_RESULT' || errorKey === 'SHEET_NOT_FOUND') {
          status = 404;
        } else if (errorKey === 'FEES_UNPAID') {
          status = 403;
        }

        return NextResponse.json(
          { error: mappedError },
          { status }
        );
      }

      // ── Check for no-student result ──
      const stn = String(data.stn || '').trim();
      if (!stn) {
        return NextResponse.json(
          { error: getErrorMessage('NO_RESULT') },
          { status: 404 }
        );
      }

      // ── Sanitize response — only allow whitelisted fields ──
      const sanitized = sanitizeResponse(data);

      // ── Explicit no-store for PII protection ──
      return NextResponse.json(sanitized, {
        headers: {
          'Cache-Control': 'private, no-store',
        },
      });
    }

    return NextResponse.json(
      { error: getErrorMessage('UNKNOWN_ERROR') },
      { status: 400 }
    );
  } catch (error) {
    if (IS_PRODUCTION) {
      console.error('API proxy error');
    } else {
      console.error('API proxy error:', error);
    }
    return NextResponse.json(
      { error: getErrorMessage('CONNECTION_ERROR') },
      { status: 500 }
    );
  }
}
